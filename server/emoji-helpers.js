// Processa reply_markup: anima botГөes com mapping, mantГ©m emoji Unicode nos outros
function processReplyMarkup(replyMarkup) {
  if (!replyMarkup || !replyMarkup.inline_keyboard) return replyMarkup;
  
  for (const row of replyMarkup.inline_keyboard) {
    for (const button of row) {
      if (button.text && !button.icon_custom_emoji_id) {
        const match = button.text.match(EMOJI_REGEX);
        if (match) {
          const emoji = match[0];
          const emojiId = getCustomEmojiId(emoji);
          if (emojiId) {
            button.icon_custom_emoji_id = emojiId;
            button.text = button.text.replace(emoji, '').trim();
          }
          // Se emojiId Г© null, mantГ©m o emoji no texto como Unicode
        }
      }
    }
  }
  
  return replyMarkup;
}

// Wrapper para sendMessage que adiciona emojis animados automaticamente
function wrapSendWithEmojis(originalSend) {
  return function(chatId, text, options = {}) {
    if (options.reply_markup) {
      options.reply_markup = processReplyMarkup(options.reply_markup);
    }
    return originalSend.call(this, chatId, text, options);
  };
}

